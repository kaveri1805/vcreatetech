    <script>
        const categories = [
            "SEO", "SMO", "WEB DESIGN", "GRAPHIC DESIGN",
            "WORDPRESS", "JOOMLA", "DRUPAL",
            "CMS",

        ];

        const categoryLinks = {
            "SEO": "seo.php",
            "SMO": "smo.php",
            "WEB DESIGN": "web-designing.php",
            "GRAPHIC DESIGN": "graphic-designing.php",
            "WORDPRESS": "wordpressweb.php",
            "JOOMLA": "joomla-web.php",
            "DRUPAL": "drupal.php",
            "CMS": "cms-webdev.php",

        };

        const itemsPerPage = 5;
        let startIndex = 0;
        let selectedCategory = '';

        const categoryList = document.getElementById('categoryList');
        const prevBtn = document.getElementById('prevBtn');
        const nextBtn = document.getElementById('nextBtn');

        function renderCategories() {
            categoryList.innerHTML = '';
            const visible = categories.slice(startIndex, startIndex + itemsPerPage);
            visible.forEach(category => {
                const span = document.createElement('span');
                span.className = 'category';
                if (category === selectedCategory) {
                    span.classList.add('selected');
                }
                span.innerText = category;
                span.addEventListener('click', () => {
                    selectedCategory = category;
                    renderCategories();
                    // ✅ Navigate to corresponding page
                    const page = categoryLinks[category];
                    if (page) {
                        window.location.href = page;
                    } else {
                        alert("Page not found for: " + category);
                    }
                });
                categoryList.appendChild(span);
            });

            prevBtn.style.visibility = startIndex === 0 ? 'hidden' : 'visible';
            nextBtn.style.visibility = (startIndex + itemsPerPage >= categories.length) ? 'hidden' : 'visible';
        }

        prevBtn.addEventListener('click', () => {
            if (startIndex > 0) {
                startIndex -= itemsPerPage;
                renderCategories();
            }
        });

        nextBtn.addEventListener('click', () => {
            if (startIndex + itemsPerPage < categories.length) {
                startIndex += itemsPerPage;
                renderCategories();
            }
        });

        renderCategories();
    </script>

