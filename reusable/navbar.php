<script>
    // Toggle main dropdown
    document.querySelector('.services-toggle').addEventListener('click', function(e) {
        e.preventDefault();
        let dropdown = this.closest('.dropdown');
        dropdown.classList.toggle('open');
    });

    // Toggle submenus â€“ allow only one open at a time
    document.querySelectorAll('.submenu-toggle').forEach(function(element) {
        element.addEventListener('click', function(e) {
            e.preventDefault();
            e.stopPropagation();

            let currentSubmenu = this.closest('.dropdown-submenu');

            // Close all other submenus
            document.querySelectorAll('.dropdown-submenu').forEach(function(submenu) {
                if (submenu !== currentSubmenu) {
                    submenu.classList.remove('open');
                }
            });

            // Toggle current submenu
            currentSubmenu.classList.toggle('open');
        });
    });
</script>